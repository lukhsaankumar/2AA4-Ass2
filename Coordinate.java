package ca.mcmaster.se2aa4.island.teamXXX;

public class Coordinate {

    private int x;
    private int y;

    public Coordinate(int xCoordinate, int yCoordinate){
        this.x = xCoordinate;
        this.y = yCoordinate;
    }
    
    public int getXCoordinate(){
        return x;
    }

    public int getYCoordinate(){
        return y;
    }
    
}